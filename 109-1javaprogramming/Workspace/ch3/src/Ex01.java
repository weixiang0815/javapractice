
public class Ex01 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {

		int age = 19;
		
		/* if (boolean) 括號內只能放 boolean 資料型態
		 * <  表示小於
		 * <= 表示小於等於
		 * == 表示等於 
		 * != 表示不等於
		 * >= 表示大於等於
		 * >  表示大於
		 */
		
		if (age >= 18) {
			System.out.println("成年");
		}
		System.out.println("程式結束");
	}

}
